        <h1 class="text-3xl font-bold text-accent-gray text-center">Act</h1>
        <div>
            <p class="mt-4">
               <strong>Share verified news:</strong> As this war is fought not only with weapons but also with information, it is very important to share information and stop the spreading of fake news. Try to report abusive, hateful or false content on social media platforms like Twitter.
            </p>
            <p class="mt-4">
                <strong>Join organizations and help groups:</strong> There are many different types of groups that help. They may collect food, blankets and other supply for Ukraine. Other groups are helping arriving refugees at airports and train stations of big cities (e.g. Berlin or Krakow).
            </p>
            <p class="mt-4">
                <strong>Find creative solutions:</strong> Many people use the knowledge of their professions to support Ukraine. IT specialists can help with cyberdefense, logistic experts can help with transportation of supply to Ukraine, photographers can take pictures of demonstrations, designers can make supportive art and influencers can call out Russia for their actions and share the truth. There’s no limit to human creativity.
            </p>
            <p class="mt-4">
                <strong>Reach out to politicians and companies:</strong> In the last days many companies have cut ties with Russia or temporarily stopped operating businesses there. It is important that many people raise their voice to hurt Russia’s economy. Write letters, emails or comments to companies and politicians and urge them to reconsider their views (e.g. drawing back from Russia).
           </p>
           <p class="mt-4">
               <strong>Demonstrate:</strong> Apply pressure on Russia on a global scale. Call for action of politics and companies. Help organizing peace protests and other actions. We can't stay quiet in these times.
