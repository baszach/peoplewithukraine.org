<h3>Статус тимчасового захисту</h3>
<div>

	https://helsinki.hu/en/information-for-people-fleeing-from-ukraine/
	http://oif.gov.hu/index.php?option=com_k2&view=item&id=1737:2022-03-04-12-27-31&Itemid=2110&lang=en

	Статус тимчасового захисту надає право на
	проживання доти, доки триває війна. Цей статус не дає права подорожувати в межах
	Європейського Союзу або проживати в іншій державі ЄС. Подати заяву
	можна особисто в будь-якому із встановлених пунктів прийому цілодобово або в будь-якому відділенні
	обслуговування клієнтів Національного Генерального Управління у Справах Іноземців в робочий час.
</div>
