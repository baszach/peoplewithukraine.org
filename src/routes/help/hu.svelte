	После того, как вы подали заявление на получение временной защиты, Вам полагается предоставляемая
	государством защита. Она включает в себя предоставление жилья, питания и медицинского ухода. Если
	жилье Вам предоставит кто-либо другой, Вы не обязаны воспользоваться предоставленным государством
	жильем, однако должны об этом уведомить ведомство тогда, когда будете обращаться за
	предоставлением статуса временной защиты. В этом случае проситель от государства получит меньше
	помощи, но на медицинский уход право имеет.Те, кто получил временную защиту, в настощеевремя могут
	устроиться на определенные рабочие места без дополнительного разрешения. В настоящее время
	просители убежища и на другие рабочие места могут получить разрешение на работу в упрощенном
	режиме. Если у просителей убежища нет жилья, государство обеспечит его им. Просителям убежища
	полагается еда, а детям обучение. Просители временной защиты получат венгерское удостоверение
	личности, которое однако отличается от удостоверения личности венгерских граждан. Якщо вам
